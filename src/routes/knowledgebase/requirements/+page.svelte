<h1>System Requirements</h1>

<p>
	<b>Operating System:</b> Windows based (e.g. Windows 7, Windows Server 2016 or higher) <br />
	<b>Database:</b> PostgreSQL (11/12/13) <br />
	<b>Hardware Requirements:</b> No Minimum; Recommendation: 2 CPU cores and 4GB RAM <br />
	<b>HDD/Space:</b> This depends on the data you would like to store and share. Most instances run with
	100GB HDD and still have enough free space
</p>
