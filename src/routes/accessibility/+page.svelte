<h2>Erklärung zur Barrierefreiheit</h2>
<p>Diese Erklärung zur Barrierefreiheit gilt für die unter https://bexis2.uni-jena.de veröffentlichte Webseite</p>

<p>Im Sinne der Richtlinie (EU) 2016/2102 sind wir bemüht, unsere Websites und mobilen Anwendungen im Einklang mit den Bestimmungen des Behindertengleichstellungsgesetzes des Bundes (BGG) 
sowie der Barrierefreien-Informationstechnik-Verordnung (BITV 2.0) zur Umsetzung der Richtlinie (EU) 2016/2102 barrierefrei zugänglich zu machen.</p>

<h3>Stand der Vereinbarkeit mit den An­for­der­ungen</h3>
<p>Die Anforderungen der Barrierefreiheit ergeben sich aus §§ 3 Absätze 1 bis 4 und 4 der BITV 2.0, die auf der Grundlage von § 12d BGG erlassen wurde. </p>

<p>Die Website ist mit den zuvor genannten Anforderungen derzeit nicht vollständig vereinbar. Die Website wird aktuell hinsichtlich der Barrierefreiheit geprüft. 
  Wir bemühen uns, alle festgestellten Probleme der Zugänglichkeit zu beheben.</p>

